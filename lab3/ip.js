'use strict'

function ipToNumber(ip) {
    const num = ip
        .split('.') //розділяє айпі ("192.168.1.10" -> ["192", "168", "1", "10"])
        .map(Number) // конвертує його в число ["192", "168", "1", "10"] -> [192, 168, 1, 10]
        .reduce((acc, num, i) => acc + (num << ((3 - i) * 8)), 0); // (3 - i) * 8 рахує на скільки біт здвинути
    return {
        decimal: num,
        hex: "0x" + (num >>> 0).toString(16).padStart(8, "0")
    }
}

